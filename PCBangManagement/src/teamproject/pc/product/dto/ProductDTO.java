package teamproject.pc.product.dto;

public class ProductDTO {
	String num;
	 String name;	 
	 int price;
	 int stock;
	
	 
	 public String getNum() {
		return num;
	}
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	public int getStock() {
		return stock;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	
	 
}
